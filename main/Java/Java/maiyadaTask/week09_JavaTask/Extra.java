package maiyadaTask.week09_JavaTask;

public class Extra {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 5;

        System.out.println("before swapping: num1 = " + num1+ ", and num2 = " + num2 );

        int temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("after swapping: num1 = " + num1+ ", and num2 = " + num2 );





    }
}
